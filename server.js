import express from "express";
import process from "node:process";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const app = express();
app.use(express.json());

// â”€â”€â”€ Logging â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
function log(message, meta = null) {
  const entry = {
    time: new Date().toISOString(),
    message,
    ...(meta ? { meta } : {})
  };
  process.stdout.write(JSON.stringify(entry) + "\n");
}

// â”€â”€â”€ Constants â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
const PORT = process.env.PORT || 3000;
const NODE_ENV = process.env.NODE_ENV || "Production";
const VERSION = "2025.10.10";

// â”€â”€â”€ Health Endpoint â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
app.get("/health", (req, res) => {
  log("ðŸ§© Health check hit");
  res.status(200).json({
    status: "ok",
    version: VERSION,
    uptime: `${Math.round(process.uptime())}s`,
    environment: NODE_ENV
  });
});

// â”€â”€â”€ Routes Import (Dynamic Safe Loader) â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
async function loadRoutes() {
  try {
    const rewriteRoutes = await import("./routes/rewrite.js");
    app.use("/api/rewrite", rewriteRoutes.default || rewriteRoutes);

    const podcastRoutes = await import("./routes/podcast.js");
    app.use("/api/podcast", podcastRoutes.default || podcastRoutes);

    const rssRoutes = await import("./routes/rss.js");
    app.use("/api/rss", rssRoutes.default || rssRoutes);

    log("âœ… Routes loaded successfully");
  } catch (err) {
    log("âŒ Failed to load one or more route modules", { error: err.message });
  }
}

// â”€â”€â”€ 404 Fallback â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
